package ExercicioClasses;

public class Animal {
    public String animal;
    public String raca;
    public String sexo;
    public String nome;

    public Animal(){
        this.animal = animal;
        this.raca = raca;
        this.sexo = sexo;
        this.nome = nome;
    }

    @Override
    public String toString() {
        return "Animal{" +
                "animal='" + animal + '\'' +
                ", raca='" + raca + '\'' +
                ", sexo='" + sexo + '\'' +
                ", nome='" + nome + '\'' +
                '}';
    }
}
